int main()
{  /* check sha256() digest results match expected */
   size_t inlen[NUMVECTORS];
   uint8_t digest[NUMVECTORS][DIGESTLEN];
   int j;

   /* init memory (synchronous) */
   memset(digest, 0, sizeof(digest));

   for (j = 0; j < NUMVECTORS; j++) {
      inlen[j] = strlen(rfc_1321_vectors[j]);
   }

   /* perform bulk hash */
   test_kcu_sha256(rfc_1321_vectors, inlen, MAXVECTORLEN, digest, NUMVECTORS);

   /* analyze results */
   for (j = 0; j < NUMVECTORS; j++) {
      ASSERT_CMP(digest[j], expect[j], DIGESTLEN);
   }
}
